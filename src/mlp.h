#ifndef MLP_H_
#define MLP_H_

#include "oac_types.h"

#define WEIGHTS_SCALE (1.f/128)

#define MAX_NEURONS 32

typedef struct {
    const oac_int8 *bias;
    const oac_int8 *input_weights;
    int nb_inputs;
    int nb_neurons;
    int sigmoid;
} AnalysisDenseLayer;

typedef struct {
    const oac_int8 *bias;
    const oac_int8 *input_weights;
    const oac_int8 *recurrent_weights;
    int nb_inputs;
    int nb_neurons;
} AnalysisGRULayer;

extern const AnalysisDenseLayer oaci_layer0;
extern const AnalysisGRULayer oaci_layer1;
extern const AnalysisDenseLayer oaci_layer2;

void oaci_analysis_compute_dense(const AnalysisDenseLayer *layer, float *output, const float *input);

void oaci_analysis_compute_gru(const AnalysisGRULayer *gru, float *state, const float *input);

#endif /* MLP_H_ */
